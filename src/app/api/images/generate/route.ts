function getPrompt(industry: string, imageType: string, customPrompt?: string): string {
  if (customPrompt) return customPrompt;
  const template = PROMPT_TEMPLATES[industry]?.[imageType];
  if (template) return template;
  // Fallback
  return `${industry}商家营销图片，${imageType}，抖音生活服务商家风格，专业摄影，高清质感`;
}

export async function POST(request: NextRequest) {
  const keyError = requireApiKey();
  if (keyError) return keyError;
  try {
    const { industry, imageType, customPrompt, size = '2K' } = await request.json();

    if (!industry || !imageType) {
      return NextResponse.json({ error: 'Missing required parameters: industry or imageType' }, { status: 400 });
    }

    const prompt = getPrompt(industry, imageType, customPrompt);

    const apiResponse = await fetch(DOUBO_API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DOUBO_API_KEY}`,
      },
      body: JSON.stringify({
        model: DOUBO_MODEL,
        prompt,
        sequential_image_generation: 'disabled',
        response_format: 'url',
        size: size === '4K' ? '4K' : '2K',
        stream: false,
        watermark: true,
      }),
    });

    if (!apiResponse.ok) {
      const errorText = await apiResponse.text();
      return NextResponse.json({ error: `API 调用失败: ${errorText}` }, { status: 502 });
    }

    const data = await apiResponse.json();

    if (!data.data?.[0]?.url) {
      return NextResponse.json({ error: 'API 返回数据格式错误', detail: data }, { status: 502 });
    }

    return NextResponse.json({
      success: true,
      imageUrl: data.data[0].url,
      size: data.data[0].size,
      prompt,
      model: DOUBO_MODEL,
    });
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : '未知错误';
    return NextResponse.json({ error: `服务器错误: ${message}` }, { status: 500 });
  }
}
